filmes_tupla = ("Interestelar", "Armagedon", "Matrix", "Ultimo Samurai", "Top Gun")
print(filmes_tupla)

# 1 - Buscar os dois primeiros itens da tupla
print(filmes_tupla[:2])

# 2 - Buscar o ultimo item da tupla
print(filmes_tupla[-1])

# 3 - Buscar itens pelo indice - ex: 1 e 2
print(filmes_tupla[1:3])

# 4 - Buscar item de uma posição em diante
print(filmes_tupla[3:])

# 5 - Buscar um item da tupla pelo nome
print(filmes_tupla.index("Matrix"))